// Suma
function suma (a, b) {
    return a + b
}

// Resta
function resta(a, b) {
    return a - b
}

// Multiplicaci√≥n
function multiplicar(a, b) {
    return a * b
}

// Division
function dividir(a, b) {
    return a / b
}

// Raiz cuadrada
function raizCuadrada(a) {
    return Math.sqrt(a)
}

// Potenciacion 
function potenciacion(base, exponente) {
    return Math.pow(base, exponente)
}